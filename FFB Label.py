from ghidra.program.model.symbol import SourceType

def apply_labels():
    program = currentProgram
    symbolTable = program.getSymbolTable() # Use the Symbol Table for DAT_ labels
    fm = program.getFunctionManager()
    
    # Apply function labels
    monitor.setMessage("Applying function labels...")
    for addr_int, name in function_labels.items():
        addr = toAddr(addr_int)
        func = fm.getFunctionAt(addr)
        if func:
            try:
                func.setName(name, SourceType.USER_DEFINED)
            except:
                pass
        else:
            # Create function if it doesn't exist
            try:
                createFunction(addr, name)
            except:
                pass

    # Apply variable labels
    monitor.setMessage("Applying variable labels...")
    for addr_int, name in variable_labels.items():
        addr = toAddr(addr_int)
        # FIX: Remove existing labels first to avoid conflicts, then create the new one
        symbols = symbolTable.getSymbols(addr)
        for s in symbols:
            symbolTable.removeSymbolSpecial(s)
        
        symbolTable.createLabel(addr, name, SourceType.USER_DEFINED)

def main():
    try:
        # Start transaction to allow Ghidra to undo changes if necessary
        txId = currentProgram.startTransaction("Apply Labels")
        apply_labels()
        currentProgram.endTransaction(txId, True)
    except Exception as e:
        print("An error occurred: {}".format(str(e)))
        currentProgram.endTransaction(txId, False)

# Run the script main function
main()
